/*
** -----------------------------------------------------------------------------
** utility
**
** If character is flag character, return 1. Otherwise return 0.
** -----------------------------------------------------------------------------
*/

#include "ft_printf.h"

int	ft_isflag(int c)
{
	if (c == '-' || c == '0')
		return (1);
	return (0);
}
