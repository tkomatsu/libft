/*
** -----------------------------------------------------------------------------
** utility
**
** If character is format character, return 1. Otherwise return 0.
** -----------------------------------------------------------------------------
*/

int	ft_isconvert(int c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'u'
		|| c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}
