#include "libft.h"

/*
** -----------------------------------------------------------------------------
** utility
**
** return the length of num with base
** -----------------------------------------------------------------------------
*/

size_t	ft_numlen(long long num, int base)
{
	size_t	len;

	if (num == 0)
		return (1);
	len = 0;
	while (num != 0)
	{
		if (num < 0)
		{
			len++;
			num *= (-1);
		}
		len++;
		num /= base;
	}
	return (len);
}
